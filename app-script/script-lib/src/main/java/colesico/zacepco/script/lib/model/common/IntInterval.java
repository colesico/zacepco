package colesico.zacepco.script.lib.model.common;

/**
 * Integer interval
 */
public class IntInterval {

    /**
     * Interval min value
     */
    public Integer min;

    /**
     * Max value
     */
    public Integer max;

    public Integer getMin() {
        return min;
    }

    public void setMin(Integer min) {
        this.min = min;
    }

    public Integer getMax() {
        return max;
    }

    public void setMax(Integer max) {
        this.max = max;
    }
}
