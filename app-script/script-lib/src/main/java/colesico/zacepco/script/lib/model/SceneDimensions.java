package colesico.zacepco.script.lib.model;

/**
 * Crime scene dimensions
 */
public class SceneDimensions {

    public Integer cols;
    public Integer rows;

    public Integer getCols() {
        return cols;
    }

    public void setCols(Integer cols) {
        this.cols = cols;
    }

    public Integer getRows() {
        return rows;
    }

    public void setRows(Integer rows) {
        this.rows = rows;
    }
}
