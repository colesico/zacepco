package colesico.zacepco.script.lib.model.entity;

/**
 * Grid size in cells
 * @see Cell
 */
public class Grid {

    public Integer rows;
    public Integer cols;

    public Integer getRows() {
        return rows;
    }

    public void setRows(Integer rows) {
        this.rows = rows;
    }

    public Integer getCols() {
        return cols;
    }

    public void setCols(Integer cols) {
        this.cols = cols;
    }
}
